package com.yichao.hackernews.model;

public class HackerNewsItem {

    private Integer id;

    private String by;

    private String url;

    private String title;

    private String type;

    private Long time;

    public Integer getId() {
        return id;
    }

    public String getBy() {
        return by;
    }

    public String getUrl() {
        return url;
    }

    public String getTitle() {
        return title;
    }

    public String getType() {
        return type;
    }

    public Long getTime() {
        return time;
    }
}
